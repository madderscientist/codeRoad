bool AES::Encrypt(const char* const iFile, const char* const oFile) {
	ifstream fin;
	fin.open(iFile, std::ios_base::in | std::ios_base::binary);
	if (!fin.is_open()) return false;

	ofstream fout;
	fout.open(oFile, std::ios_base::trunc | std::ios_base::out | std::ios_base::binary);
	if (!fout.is_open()) return false;

	std::string fileName = iFile;
	fileName = fileName.substr(fileName.find_last_of('\\') + 1) + '*';
	auto lckFN = AES::Encrypt(fileName.c_str());
	fout.write(lckFN, strlen(lckFN));
	delete[] lckFN;

	encrypt_(fin, fout);
	return true;
}

bool AES::Decipher(const char* const iFile, const char* const oPath){
	ifstream fin;
	fin.open(iFile, std::ios_base::in | std::ios_base::binary);
	if (!fin.is_open()) return false;

	std::string fileName = oPath;
	char C; bool Flag = true;
	while (Flag) {
		fin.read((char*)&c, 16);
		if (!fin.good()) return false;

		blockDecipher();
		for (int i = 0; i < 16; i++) {
			if ((C = c[i / 4][i % 4]) != '*') {
				fileName += C;
			}
			else {
				Flag = false;
				break;
			}
		}
	}

	ofstream fout;
	fout.open(fileName, std::ios_base::trunc | std::ios_base::out | std::ios_base::binary);
	if (!fout.is_open()) return false;

	decipher_(fin, fout);
	return true;
}

char* AES::Encrypt(const char* const str) {
	if (str == nullptr) return nullptr;
	std::istringstream isstr(str);
	std::ostringstream osstr;
	encrypt_(isstr, osstr);
	return sstrToCstr(osstr);
}
char* AES::Decipher(const char* const str) {
	if (str == nullptr) return nullptr;
	std::istringstream isstr(str);
	std::ostringstream osstr;
	decipher_(isstr, osstr);
	return sstrToCstr(osstr);
}

void AES::keyLoad(const char* const key) {
	for (int i = 0; i < 16; i++) Key[0][i / 4][i % 4] = key[i];

	//Key extend
	for (int t = 1; t <= 10; t++) {
		for (int j = 0; j <= 3; j++)
			Key[t][0][j] = S[Key[t - 1][3][(j + 1) % 4]];
		Key[t][0][0] ^= Rcon[t];
		for (int j = 0; j <= 3; j++)
			Key[t][0][j] ^= Key[t - 1][0][j];

		for (int i = 1; i <= 3; i++)
			for (int j = 0; j <= 3; j++)
				Key[t][i][j] = Key[t - 1][i][j] ^ Key[t][i - 1][j];
	}
}

