void encrypt_(istream& fin, ostream& fout) {
	int size = 16 - getLen(fin) & 15;

	while (fin.good()) {
		fin.read((char*)&c, 16);
		if (fin.eof()) {
			for (int i = 15; i > 15 - size; i--) c[i / 4][i % 4] = size;
		}
		blockEncrypt();
		fout.write((char*)&c, 16);
	}
}

bool decipher_(istream& fin, ostream& fout) {
	auto size = getLen(fin);
	if (size % 16 != 0) return false;
	else size /= 16;

	for (ULL i = 0; i + 2 <= size; i++) {
		fin.read((char*)&c, 16);
		blockDecipher();
		fout.write((char*)&c, 16);
	}
	fin.read((char*)&c, 16);
	blockDecipher();
	fout.write((char*)&c, (16) - c[3][3]);
	return true;
}
