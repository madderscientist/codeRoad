void byteChange() {
	for (int i = 0; i <= 3; i++)
		for (int j = 0; j <= 3; j++)
			c[i][j] = S[c[i][j]];
}
void byteChange2() {
	for (int i = 0; i <= 3; i++)
		for (int j = 0; j <= 3; j++)
			c[i][j] = S2[c[i][j]];
}

void rowChange() {
	for (int i = 1; i <= 3; i++)
		for (int j = 0; j <= 3; j++)
			tpc[i][j] = c[(j + i) % 4][i];
	for (int i = 1; i <= 3; i++)
		for (int j = 0; j <= 3; j++)
			c[j][i] = tpc[i][j];
}
void rowChange2() {
	for (int i = 1; i <= 3; i++)
		for (int j = 0; j <= 3; j++)
			tpc[i][j] = c[(j - i + 4) % 4][i];
	for (int i = 1; i <= 3; i++)
		for (int j = 0; j <= 3; j++)
			c[j][i] = tpc[i][j];
}

void colMix() {
	for (int i = 0; i <= 3; i++)
		for (int j = 0; j <= 3; j++)
			tpc[i][j] = mult(Lmix[i][0], c[j][0]) ^
			mult(Lmix[i][1], c[j][1]) ^
			mult(Lmix[i][2], c[j][2]) ^
			mult(Lmix[i][3], c[j][3]);
	for (int i = 0; i <= 3; i++)
		for (int j = 0; j <= 3; j++)
			c[j][i] = tpc[i][j];
}
void colMix2() {
	for (int i = 0; i <= 3; i++)
		for (int j = 0; j <= 3; j++)
			tpc[i][j] = mult(Lmix2[i][0], c[j][0]) ^
			mult(Lmix2[i][1], c[j][1]) ^
			mult(Lmix2[i][2], c[j][2]) ^
			mult(Lmix2[i][3], c[j][3]);
	for (int i = 0; i <= 3; i++)
		for (int j = 0; j <= 3; j++)
			c[j][i] = tpc[i][j];
}

void keyAdd(int turn) {
	for (int i = 0; i <= 3; i++)
		for (int j = 0; j <= 3; j++)
			c[i][j] ^= Key[turn][i][j];
}

void blockEncrypt() {
	for (int i = 0; i <= TN - 2; i++) {
		keyAdd(i);
		byteChange();
		rowChange();
		colMix();
	}
	keyAdd(TN - 1);
	byteChange();
	rowChange();
	keyAdd(TN);
}
void blockDecipher() {
	keyAdd(TN);
	rowChange2();
	byteChange2();
	keyAdd(TN - 1);
	for (int i = TN - 2; i >= 0; i--) {
		colMix2();
		rowChange2();
		byteChange2();
		keyAdd(i);
	}
}

